                        <p class="txtPage">Your recreational vehicle sees it all, highways, dirt roads, and the woods.
                            It’s important to keep your RV clean and looking good in the face of adversity. In these
                            conditions your vehicle needs protection that goes above and beyond.</p>

                        <p class="txtPage">System X is the latest ceramic resin technology. Not a clear bra, wax, polish
                            or typical paint sealant. This new coating exhibits outstanding resistance to the UV rays,
                            friction, heat, and bird droppings. System X seals, protects, and bonds to all your RV's
                            surfaces, including composites.</p>

                        <p class="txtPage">System X is a semi-permanent ceramic coating which bonds directly with your
                            motorhome’s paint. Relative to your RV’s size, System X is easily applied by hand in about
                            30 minutes you’ll have provided your paintwork with long-term shine and protection.</p>

                        <p class="txtPage">System X is a semi-permanent coating that actually becomes the functional
                            surface of your vehicle. By creating an inseparable bond on a molecular level with the
                            surface you get a coating that cannot be washed off like a wax or sealant.</p>

                        <p class="txtPage">System X acts as a clear coat, which provides increased protection against
                            the elements. This new clear coating is glossier, chemical resistant, and harder than the
                            original paint. Providing incredible protection against de-icing agents, minor scratches,
                            swirl marks, acid rain, bird droppings, and UV rays.</p>


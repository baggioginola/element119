                        <p class="txtPage">System X is the latest ceramic resin technology. Not a clear bra, wax, polish
                            or typical paint sealant. This new coating exhibits outstanding resistance to friction,
                            heat, and solvents. System X seals, protects, and bonds to all painted aircraft surfaces,
                            including composites.</p>

                        <p class="txtPage">A breakthrough in aviation protection technology, System X is a
                            semi-permanent ceramic coating. It is easily applied by hand in about 30 minutes to your
                            helicopter, plane, or jet to provide long-term shine and protection.</p>

                        <p class="txtPage">System X is a semi-permanent coating that actually becomes the functional
                            surface of your aircraft’s paintwork. By creating an inseparable bond on a molecular level
                            with your paintwork you get a coating that cannot be washed off like a wax or sealant.</p>

                        <p class="txtPage">System X acts as a second clear coat, which provides increased protection
                            against the elements. This new clear coating is glossier, chemical resistant, and harder
                            than the original clear coat. Providing incredible protection against de-icing fluids, minor
                            scratches, swirl marks, acid rain, bird droppings, and UV rays.</p>

                        <p class="txtPage">This coating doesn’t just form a sacrificial shield of protection like other
                            products. Instead, it bonds into your paintwork or metal surface creating a semi-permanent
                            shell.</p>


                            polish or typical paint sealant. This new coating exhibits outstanding resistance to salt
                            water, friction, and solvents. System X seals, protects, and bonds to all painted marine
                            surfaces including composites.</p>

                        <p class="txtPage">A breakthrough in marine protection technology, System X is a semi-permanent
                            ceramic coating which bonds directly with gel-coat. Relative to size it is easily applied by
                            hand in about 30 minutes to your boat, yacht, or ship to provide long-term shine and
                            protection.</p>

                        <p class="txtPage">System X is a semi-permanent coating that actually becomes the functional
                            surface of your craft’s hull and other paintwork. By creating an inseparable bond on a
                            molecular level with your surface you get a coating that cannot be washed off like a wax or
                            sealant.</p>


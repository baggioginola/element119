            <td>
            <p class="txtPage">In the last 20 years paint sealants were introduced to the world of paint protection. Typically these sealants are derived from amino functional fluids in the form of polymers. They give a good initial shine, which can last 6 months, sometimes longer. The thin protective coating of this type of product is ineffective when cleaners containing solvents or the corrosive natural chemicals such as those found in birdlime and tree sap contact the surface.</p>
            </td>
            </tr>

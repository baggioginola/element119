            <td valign="top">
                <p class="txtPage">Generation 3</p>
            </td>
            <td>
            <p class="txtPage">System X - A brand new innovation in paint protection. The no-paste, no-wax formula is specially designed to work on all the latest paint finishes and varnishes. The active ceramic resin binds with complex polymers to provide a thickness and hardness unachievable until now. System X Resin can be used on all exterior paint surfaces including alloys. By bonding into the surface pores and forming an extremely durable semi-permanent coating System X provides genuine long-term protection, which requires no maintenance other than washing.</p>

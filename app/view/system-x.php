                <p class="txtPage">During the last half-century car waxes have ruled the market. Ranging from naturally occurring waxes like carnauba to man-made synthetic waxes. While imparting gloss and a sacrificial barrier of protection their effect is short lived. Environmental hazards quickly break down and remove traditional waxes leaving paintwork exposed to damage.</p>
